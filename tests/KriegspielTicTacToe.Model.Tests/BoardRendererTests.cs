  ├───┼───┼───┤
  │ 1 │ 2 │ 3 │
  └───┴───┴───┘
"
            .Substring(Environment.NewLine.Length) //skip the leading linebreak needed for legibility
            .TrimEnd()
            .ReplaceLineEndings();

        actual.TrimEnd().Should().Be(expected);
    }

